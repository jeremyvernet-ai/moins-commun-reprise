create extension if not exists "pgcrypto";

drop view if exists public.song_details;
drop table if exists public.song_relationships cascade;
drop table if exists public.song_submissions cascade;
drop table if exists public.favorites cascade;
drop table if exists public.songs cascade;
drop table if exists public.artists cascade;
drop table if exists public.profiles cascade;

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text unique,
  avatar_url text,
  is_admin boolean not null default false,
  created_at timestamptz not null default now()
);

create table public.artists (
  id bigint generated by default as identity primary key,
  name text not null unique,
  country text,
  bio text,
  created_at timestamptz not null default now()
);

create table public.songs (
  id bigint generated by default as identity primary key,
  title text not null,
  artist_id bigint references public.artists(id) on delete set null,
  release_year integer,
  genre text,
  description text,
  cover_url text,
  status text not null default 'published' check (status in ('draft','published')),
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now()
);

create table public.song_relationships (
  id bigint generated by default as identity primary key,
  primary_song_id bigint not null references public.songs(id) on delete cascade,
  related_song_id bigint not null references public.songs(id) on delete cascade,
  relation_type text not null check (
    relation_type in (
      'sampled','sampled_by',
      'cover_of','covered_by',
      'remix_of','remixed_by',
      'interpolation_of','interpolated_by'
    )
  ),
  notes text,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  check (primary_song_id <> related_song_id)
);

create table public.favorites (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  song_id bigint not null references public.songs(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (user_id, song_id)
);

create table public.song_submissions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  title text not null,
  artist_id bigint references public.artists(id) on delete set null,
  artist_name_text text,
  release_year integer,
  genre text,
  description text,
  cover_url text,
  relation_type text check (
    relation_type in (
      'sampled','sampled_by',
      'cover_of','covered_by',
      'remix_of','remixed_by',
      'interpolation_of','interpolated_by'
    )
  ),
  related_song_id bigint references public.songs(id) on delete set null,
  status text not null default 'pending' check (status in ('pending','approved','rejected')),
  approved_song_id bigint references public.songs(id) on delete set null,
  reviewed_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now()
);

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, username)
  values (new.id, coalesce(new.raw_user_meta_data->>'username', split_part(new.email, '@', 1)))
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure public.handle_new_user();

create or replace function public.is_admin(check_user uuid)
returns boolean
language sql
security definer
set search_path = public
as $$
  select coalesce((select is_admin from public.profiles where id = check_user), false);
$$;

create view public.song_details as
select
  s.id,
  s.title,
  s.release_year,
  s.genre,
  s.description,
  s.cover_url,
  s.status,
  s.created_at,
  a.id as artist_id,
  a.name as artist_name
from public.songs s
left join public.artists a on a.id = s.artist_id;

