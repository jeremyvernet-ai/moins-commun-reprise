create policy "profiles readable by everyone"
on public.profiles for select
to authenticated, anon
using (true);

create policy "profiles users can update themselves"
on public.profiles for update
to authenticated
using (auth.uid() = id)
with check (auth.uid() = id);

create policy "artists readable by everyone"
on public.artists for select
to authenticated, anon
using (true);

create policy "artists admins can write"
on public.artists for all
to authenticated
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "songs published readable by everyone"
on public.songs for select
to authenticated, anon
using (status = 'published' or public.is_admin(auth.uid()));

create policy "songs admins can write"
on public.songs for all
to authenticated
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "relationships readable by everyone"
on public.song_relationships for select
to authenticated, anon
using (
  exists (
    select 1 from public.songs s
    where s.id = primary_song_id
    and (s.status = 'published' or public.is_admin(auth.uid()))
  )
);

create policy "relationships admins can write"
on public.song_relationships for all
to authenticated
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "favorites user can read own"
on public.favorites for select
to authenticated
using (auth.uid() = user_id);

create policy "favorites user can insert own"
on public.favorites for insert
to authenticated
with check (auth.uid() = user_id);

create policy "favorites user can delete own"
on public.favorites for delete
to authenticated
using (auth.uid() = user_id);

create policy "submissions users can read own or admins"
on public.song_submissions for select
to authenticated
using (auth.uid() = user_id or public.is_admin(auth.uid()));

create policy "submissions authenticated can insert own"
on public.song_submissions for insert
to authenticated
with check (auth.uid() = user_id);

create policy "submissions admins can update"
on public.song_submissions for update
to authenticated
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

